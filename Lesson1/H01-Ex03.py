x = bool(int(input("Enter the value 1 or 0 for x (True/False): ")))
y = bool(int(input("Enter the value 1 or 0 for y (True/False): ")))

print('Boolean "x" is: ', x)
print('Boolean "y" is: ', y)
print('Boolean "x and y" is:', x and y)
print('Boolean "x or y" is:', x or y)
print('Boolean "not x" is:', not x)
print('Boolean "not y" is:', not y)